    private void jbtLogoffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtLogoffActionPerformed
        int confirmarSaida = JOptionPane.showConfirmDialog(null, "Deseja realmente sair?", "CofrePessoal", JOptionPane.YES_NO_OPTION);
        
        if (confirmarSaida == JOptionPane.YES_OPTION)
        {
            this.dispose();
            logoff(true);
        }
    }//GEN-LAST:event_jbtLogoffActionPerformed

