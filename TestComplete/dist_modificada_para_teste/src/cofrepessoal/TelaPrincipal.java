    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow() > -1)
        {
            jbtExcluir.setEnabled(true);
            jbtEditar.setEnabled(true);
        }
        else
        {
            jbtExcluir.setEnabled(false);
            jbtEditar.setEnabled(false);
        }
        
        if (jTable1.getSelectedRow() > -1 && evt.getClickCount() == 2)
        {
            TelaEditar.alterarLogins.validarLogin = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
            TelaEditar.alterarLogins.alterarDescricao = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
            TelaEditar.alterarLogins.alterarLogin = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
            TelaEditar.alterarLogins.alterarSenha = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
            TelaEditar tEditar = new TelaEditar();
            tEditar.setVisible(true);    
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jbtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtExcluirActionPerformed
        excluirCON excluirCON = new excluirCON();
        excluirCON.excluirConexao(jTable1);
        
        if (TelaPrincipal.tabelaLogins.confirmaExclusao == true)
        {
        jbtExcluir.setEnabled(false);
        jbtEditar.setEnabled(false);
        }
    }//GEN-LAST:event_jbtExcluirActionPerformed

